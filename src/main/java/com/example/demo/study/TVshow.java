package com.example.demo.study;

public class TVshow {
    private String name;
    private int id;

    public TVshow() {

    }

    public TVshow(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

}
